# 算数（数学）运算符
print("1 + 1 = ",1 + 1) # 加
print("1 - 1 = ",1 - 1) # 减
print("5 * 5 = ",5 * 5) # 乘
print("6 / 2 = ",6 / 2) # 除
print("11 // 2 = ",11 // 2) # 取整除
print("9 % 2 = ",9 % 2) # 取余
print("2 ** 3 = ",2 ** 3) # 指数


# 复合运算符
# 例：c += a 等效于 c = c + a
num = 10

num += 2
print("num += 2 =",num)
num -= 3
print("num -= 3 =",num)
num *= 5
print("num *= 5 =",num)
num /= 3
print("num /= 3 =",num)
num %= 2
print("num %= 2 =",num)

num = 9

num //= 3
print("num //= 3 =",num)
num **= 12
print("num **= 12 =",num)
