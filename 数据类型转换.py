# 数字类型转换为字符串
num_str = str(114514)
print(type(num_str),num_str)

float_str = str(35.99)
print(type(float_str),float_str)


# 字符串类型转换为数字类型
num = int("1145")
print(type(num),num)

num2 = float("35.9")
print(type(num2),num2)


# 整数转浮点数
float_num = float(1145)
print(type(float_num),float_num)


# 浮点数转整数
int_num = int(35.9)
print(type(int_num),int_num)